def build_regression_head(input_dim):
    """
    Multi-task regression network.

    Architecture
    ------------
    Input (2688-d feature vector)
        Feature compression block
            Dense(512) → BatchNorm → Swish → Dropout(0.1)
            Dense(128) → BatchNorm → Swish
        Shared trunk
            Dense(256) → ReLU → Dropout(0.1)
            Dense(128) → ReLU
        Task-specific heads (one per output)
            Dense(64)  → ReLU → Dense(1, linear)

    Parameters
    ----------
    input_dim : int
        Dimensionality of the pooled feature vector (2688 for layer 464).

    Returns
    -------
    keras.Model with outputs [kappa, mob]
    """
    inp = Input(shape=(input_dim,))

    # feature compression block
    x = Dense(512)(inp)
    x = BatchNormalization()(x)
    x = Activation("swish")(x)
    x = Dropout(DROPOUT)(x)
    x = Dense(128)(x)
    x = BatchNormalization()(x)
    x = Activation("swish")(x)

    # shared trunk
    x = Dense(256, activation="relu")(x)
    x = Dropout(DROPOUT)(x)
    x = Dense(128, activation="relu")(x)

    # task-specific heads
    kappa_out = Dense(64, activation="relu")(x)
    kappa_out = Dense(1, activation="linear", name="kappa")(kappa_out)

    mob_out = Dense(64, activation="relu")(x)
    mob_out = Dense(1, activation="linear", name="mob")(mob_out)

    return Model(inputs=inp, outputs=[kappa_out, mob_out])


