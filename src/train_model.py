if __name__ == "__main__":

    t_start = time.time()

    print("=" * 70)
    print("  Fe-Cr CNN Inverse Parameter Estimation")
    print("=" * 70)
    print(f"  GPU        : {tf.config.list_physical_devices('GPU')}")
    print(f"  Python     : {sys.version.split()[0]}")
    print(f"  TensorFlow : {tf.__version__}")
    print(f"  Seed       : {SEED}")
    print("=" * 70)

    # ------------------------------------------------------------------
    # 5.1  Load dataset
    # ------------------------------------------------------------------
    df = load_dataset(CSV_PATH)

    # ------------------------------------------------------------------
    # 5.2  Build EfficientNetB7 feature extractor (frozen)
    # ------------------------------------------------------------------
    print(f"[INFO] Loading EfficientNetB7 truncated at layer {LAYER}...")
    backbone  = EfficientNetB7(weights="imagenet", include_top=False)
    pre_model = Model(
        inputs  = backbone.inputs,
        outputs = backbone.layers[LAYER].output
    )

    # ------------------------------------------------------------------
    # 5.3  Extract features for all images
    # ------------------------------------------------------------------
    features = extract_features(df, BASE, pre_model, IMG_SIZE)

    # ------------------------------------------------------------------
    # 5.4  Dataset-level similarity diagnostics (pre-split, diagnostic only)
    # ------------------------------------------------------------------
    sim_df = compute_similarity_metrics(df, features, SAVE_DIR, LAYER)

    # ------------------------------------------------------------------
    # 5.5  Stratified train / val / test split
    # ------------------------------------------------------------------
    train_df, val_df, test_df, trainX_raw, valX_raw, testX_raw = stratified_split(
        df, features,
        val_frac  = VAL_FRAC,
        test_frac = TEST_FRAC,
        seed      = SEED,
        n_bins    = N_BINS
    )

    # ------------------------------------------------------------------
    # 5.6  Feature standardization — fit on train only to prevent leakage
    # ------------------------------------------------------------------
    scaler = StandardScaler()
    trainX = scaler.fit_transform(trainX_raw)
    valX   = scaler.transform(valX_raw)
    testX  = scaler.transform(testX_raw)

    train_kappa = train_df["kappa"].values;  train_mob = train_df["mob"].values
    val_kappa   = val_df["kappa"].values;    val_mob   = val_df["mob"].values
    test_kappa  = test_df["kappa"].values;   test_mob  = test_df["mob"].values

    # ------------------------------------------------------------------
    # 5.7  Build and compile model
    # ------------------------------------------------------------------
    print("[INFO] Building regression model...")
    model = build_regression_head(trainX.shape[1])

    model.compile(
        optimizer    = tf.keras.optimizers.Adam(learning_rate=LR),
        loss         = {
            "kappa" : tf.keras.losses.Huber(delta=DELTA_K),
            "mob"   : tf.keras.losses.Huber(delta=DELTA_M)
        },
        loss_weights = {"kappa": WEIGHT_K, "mob": WEIGHT_M},
        metrics      = {"kappa": ["mae"], "mob": ["mae"]}
    )

    model.summary()

    # ------------------------------------------------------------------
    # 5.8  Callbacks
    # ------------------------------------------------------------------
    best_weights = os.path.join(SAVE_DIR, f"weights_best_layer{LAYER}.weights.h5")

    callbacks = [
        ModelCheckpoint(
            best_weights,
            monitor           = "val_loss",
            save_best_only    = True,
            save_weights_only = True,
            verbose           = 1
        ),
        ReduceLROnPlateau(
            monitor  = "val_loss",
            factor   = LR_FACTOR,
            patience = LR_PATIENCE,
            min_lr   = LR_MIN,
            verbose  = 1
        ),
        EarlyStopping(
            monitor              = "val_loss",
            patience             = ES_PATIENCE,
            min_delta            = 1e-10,
            restore_best_weights = False   # weights loaded explicitly after training
        )
    ]

    # ------------------------------------------------------------------
    # 5.9  Build tf.data pipelines
    # ------------------------------------------------------------------
    ds_train = (
        tf.data.Dataset
        .from_tensor_slices((trainX, {"kappa": train_kappa, "mob": train_mob}))
        .shuffle(buffer_size=len(trainX), seed=SEED, reshuffle_each_iteration=True)
        .batch(BATCH)
    )

    ds_val = (
        tf.data.Dataset
        .from_tensor_slices((valX, {"kappa": val_kappa, "mob": val_mob}))
        .batch(BATCH)
    )

    # ------------------------------------------------------------------
    # 5.10  Training
    # ------------------------------------------------------------------
    print(f"[INFO] Training — max {EPOCHS} epochs, "
          f"early stopping patience = {ES_PATIENCE}...")

    history = model.fit(
        ds_train,
        validation_data = ds_val,
        epochs          = EPOCHS,
        callbacks       = callbacks,
        verbose         = 1
    )

    # ------------------------------------------------------------------
    # 5.11  Save training history
    # ------------------------------------------------------------------
    train_loss = history.history["loss"]
    val_loss   = history.history["val_loss"]
    epochs_ran = range(1, len(train_loss) + 1)

    best_epoch = int(np.argmin(val_loss)) + 1
    best_val   = float(np.min(val_loss))

    print(f"\n── Training summary ──────────────────────────")
    print(f"   Epochs run       : {len(train_loss)}")
    print(f"   Best epoch       : {best_epoch}")
    print(f"   Best val loss    : {best_val:.6f}")
    print(f"   Final train loss : {train_loss[-1]:.6f}")
    print(f"   Final val loss   : {val_loss[-1]:.6f}")
    print(f"──────────────────────────────────────────────\n")

    # loss curve plot
    fig, ax = plt.subplots(figsize=(8, 5))
    ax.plot(epochs_ran, train_loss, color="#2171b5", linewidth=1.5, label="Training loss")
    ax.plot(epochs_ran, val_loss,   color="#e8471a", linewidth=1.5, linestyle="--", label="Validation loss")
    ax.axvline(x=best_epoch, color="#41ab5d", linewidth=1.2, linestyle=":",
               label=f"Best epoch ({best_epoch})")
    ax.scatter([best_epoch], [best_val], color="#41ab5d", zorder=5, s=50)
    ax.set_xlabel("Epoch",          fontsize=12)
    ax.set_ylabel("Loss (Huber)",   fontsize=12)
    ax.set_title(f"Training and Validation Loss  |  Layer {LAYER}", fontsize=13)
    ax.legend(fontsize=11)
    ax.grid(True, alpha=0.4)
    fig.tight_layout()
    fig.savefig(os.path.join(SAVE_DIR, f"loss_curve_layer{LAYER}.png"), dpi=300)
    plt.close(fig)

    # loss history CSV + pickle
    pd.DataFrame({
        "epoch"      : list(epochs_ran),
        "loss"       : train_loss,
        "val_loss"   : val_loss
    }).to_csv(os.path.join(SAVE_DIR, f"loss_history_layer{LAYER}.csv"), index=False)

    with open(os.path.join(SAVE_DIR, f"history_layer{LAYER}.pkl"), "wb") as f:
        pickle.dump(history.history, f)

    print("[INFO] Loss curve, CSV, and pickle saved.")

    # ------------------------------------------------------------------
    # 5.12  Evaluation on held-out test set
    # ------------------------------------------------------------------
    print("\n[INFO] Evaluating on held-out test set...")
    model.load_weights(best_weights)

    pred_kappa, pred_mob = model.predict(testX)
    pred_kappa = pred_kappa.flatten()
    pred_mob   = pred_mob.flatten()

    r2_k  = r2_score(test_kappa, pred_kappa)
    mse_k = mean_squared_error(test_kappa, pred_kappa)
    mae_k = mean_absolute_error(test_kappa, pred_kappa)

    r2_m  = r2_score(test_mob, pred_mob)
    mse_m = mean_squared_error(test_mob, pred_mob)
    mae_m = mean_absolute_error(test_mob, pred_mob)

    print(f"\n── Test set performance ──────────────────────")
    print(f"   Kappa    R²  : {r2_k:.4f}")
    print(f"   Kappa    MSE : {mse_k:.4f}")
    print(f"   Kappa    MAE : {mae_k:.4f}")
    print(f"   Mobility R²  : {r2_m:.4f}")
    print(f"   Mobility MSE : {mse_m:.4f}")
    print(f"   Mobility MAE : {mae_m:.4f}")
    print(f"──────────────────────────────────────────────\n")

    parity_plot(test_kappa, pred_kappa, 0, 11,
                f"kappa  |  R²={r2_k:.3f}",
                os.path.join(SAVE_DIR, f"parity_kappa_layer{LAYER}.png"))

    parity_plot(test_mob, pred_mob, 0, 11,
                f"Mobility  |  R²={r2_m:.3f}",
                os.path.join(SAVE_DIR, f"parity_mob_layer{LAYER}.png"))

    # ------------------------------------------------------------------
    # 5.13  Prediction error table
    # ------------------------------------------------------------------
    err_kappa = np.abs(test_kappa - pred_kappa)
    err_mob   = np.abs(test_mob   - pred_mob)

    error_df = pd.DataFrame({
        "image_name" : test_df["image_name"].values,
        "kappa_gt"   : test_kappa,
        "kappa_pred" : pred_kappa,
        "err_kappa"  : err_kappa,
        "mob_gt"     : test_mob,
        "mob_pred"   : pred_mob,
        "err_mob"    : err_mob,
        "err_total"  : err_kappa + err_mob
    })

    error_df.to_csv(os.path.join(SAVE_DIR, "prediction_errors.csv"), index=False)
    print("[INFO] Prediction errors saved.")

    for col, label in [("err_kappa", "kappa"), ("err_mob", "mobility"), ("err_total", "total")]:
        top = error_df.sort_values(col, ascending=False).head(10)
        print(f"\n── Top 10 worst {label} predictions ─────────")
        print(top[["image_name", col]].to_string(index=False))

    # ------------------------------------------------------------------
    # 5.14  Error vs similarity analysis
    # ------------------------------------------------------------------
    merged = error_df.merge(sim_df, on="image_name", how="left")
    merged.to_csv(
        os.path.join(SAVE_DIR, f"errors_with_similarity_layer{LAYER}.csv"),
        index=False
    )

    corr_uni = np.corrcoef(merged["err_total"], merged["uniqueness"])[0, 1]
    corr_sim = np.corrcoef(merged["err_total"], merged["max_sim"])[0, 1]
    print(f"\n── Error-similarity correlations ─────────────")
    print(f"   Corr(total error, uniqueness) : {corr_uni:.4f}")
    print(f"   Corr(total error, max_sim)    : {corr_sim:.4f}")
    print(f"──────────────────────────────────────────────\n")

    for x_col, xlabel, fname in [
        ("uniqueness", "Uniqueness (1 - max similarity)", "err_vs_uniqueness"),
        ("max_sim",    "Max cosine similarity",            "err_vs_maxsim")
    ]:
        fig, ax = plt.subplots()
        ax.scatter(merged[x_col], merged["err_total"], alpha=0.7)
        ax.set_xlabel(xlabel)
        ax.set_ylabel(r"Total absolute error ($|\Delta\kappa| + |\Delta M|$)")
        ax.grid(True, alpha=0.3)
        fig.tight_layout()
        fig.savefig(os.path.join(SAVE_DIR, f"{fname}_layer{LAYER}.png"), dpi=200)
        plt.close(fig)

    print("[INFO] Error-similarity plots saved.")

    # ------------------------------------------------------------------
    # 5.15  Experimental image prediction
    # ------------------------------------------------------------------
    print("\n[INFO] Predicting on experimental HAADF-STEM image...")
    print(f"       {EXP_PATH}")

    exp_img    = load_img(EXP_PATH, target_size=IMG_SIZE)
    exp_img    = imagenet_utils.preprocess_input(
                     np.expand_dims(img_to_array(exp_img), axis=0))
    exp_feats  = pre_model.predict(exp_img)
    exp_flat   = pool_features(exp_feats)         # same pooling as training
    exp_scaled = scaler.transform(exp_flat)        # same scaler as training

    pred_k_exp = float(model.predict(exp_scaled)[0].flatten()[0])
    pred_m_exp = float(model.predict(exp_scaled)[1].flatten()[0])

    print(f"\n── Experimental image prediction ─────────────")
    print(f"   Predicted kappa    : {pred_k_exp:.4f}  (x10^-16 J m^2 mol^-1)")
    print(f"   Predicted mobility : {pred_m_exp:.4f}  (x10^-26 m^2 mol J^-1 s^-1)")
    print(f"──────────────────────────────────────────────\n")

    out_txt = os.path.join(SAVE_DIR, "experimental_prediction.txt")
    with open(out_txt, "w") as f:
        f.write("Experimental Image Prediction\n")
        f.write("-" * 40 + "\n")
        f.write(f"Image path         : {EXP_PATH}\n")
        f.write(f"Predicted kappa    : {pred_k_exp:.6f}  (x10^-16 J m^2 mol^-1)\n")
        f.write(f"Predicted mobility : {pred_m_exp:.6f}  (x10^-26 m^2 mol J^-1 s^-1)\n")
    print(f"[INFO] Experimental prediction saved: {out_txt}")

    # ------------------------------------------------------------------
    # 5.16  Off-grid interpolation generalization test
    # ------------------------------------------------------------------
    print("\n" + "=" * 70)
    print("  OFF-GRID INTERPOLATION GENERALIZATION TEST")
    print("=" * 70)

    interp_df = load_dataset(INTERP_CSV)

    print(f"\n── Off-grid dataset summary ──────────────────")
    print(f"   Samples        : {len(interp_df)}")
    print(f"   Kappa range    : {interp_df['kappa'].min()} to {interp_df['kappa'].max()}")
    print(f"   Mobility range : {interp_df['mob'].min()} to {interp_df['mob'].max()}")
    print(f"──────────────────────────────────────────────\n")

    # feature extraction and standardization use the same pipeline as training
    interp_feats  = extract_features(interp_df, INTERP_DIR, pre_model, IMG_SIZE)
    interp_scaled = scaler.transform(interp_feats)   # transform only — never refit

    model.load_weights(best_weights)
    pred_k_interp, pred_m_interp = model.predict(interp_scaled)
    pred_k_interp = pred_k_interp.flatten()
    pred_m_interp = pred_m_interp.flatten()

    r2_k_interp  = r2_score(interp_df["kappa"].values, pred_k_interp)
    mae_k_interp = mean_absolute_error(interp_df["kappa"].values, pred_k_interp)
    mse_k_interp = mean_squared_error(interp_df["kappa"].values, pred_k_interp)

    r2_m_interp  = r2_score(interp_df["mob"].values, pred_m_interp)
    mae_m_interp = mean_absolute_error(interp_df["mob"].values, pred_m_interp)
    mse_m_interp = mean_squared_error(interp_df["mob"].values, pred_m_interp)

    print(f"\n── Off-grid test results ─────────────────────")
    print(f"   Kappa    R²  : {r2_k_interp:.4f}")
    print(f"   Kappa    MSE : {mse_k_interp:.4f}")
    print(f"   Kappa    MAE : {mae_k_interp:.4f}")
    print(f"   Mobility R²  : {r2_m_interp:.4f}")
    print(f"   Mobility MSE : {mse_m_interp:.4f}")
    print(f"   Mobility MAE : {mae_m_interp:.4f}")
    print(f"──────────────────────────────────────────────\n")

    print(f"\n── Grid test vs off-grid test comparison ─────")
    print(f"{'Metric':<25} {'Grid test':>15} {'Off-grid':>15}")
    print("-" * 57)
    for label, gv, ov in [
        ("Kappa R²",    r2_k,  r2_k_interp),
        ("Kappa MSE",   mse_k, mse_k_interp),
        ("Kappa MAE",   mae_k, mae_k_interp),
        ("Mobility R²", r2_m,  r2_m_interp),
        ("Mobility MSE",mse_m, mse_m_interp),
        ("Mobility MAE",mae_m, mae_m_interp),
    ]:
        print(f"   {label:<22} {gv:>15.4f} {ov:>15.4f}")
    print(f"──────────────────────────────────────────────\n")

    # save detailed results
    pd.DataFrame({
        "image_name" : interp_df["image_name"],
        "kappa_true" : interp_df["kappa"].values,
        "kappa_pred" : pred_k_interp,
        "kappa_err"  : np.abs(interp_df["kappa"].values - pred_k_interp),
        "mob_true"   : interp_df["mob"].values,
        "mob_pred"   : pred_m_interp,
        "mob_err"    : np.abs(interp_df["mob"].values - pred_m_interp),
    }).to_csv(os.path.join(SAVE_DIR, "interpolation_test_results.csv"), index=False)

    print("[INFO] Interpolation results saved.")

    parity_plot(
        interp_df["kappa"].values, pred_k_interp, 0, 11,
        f"kappa — Off-Grid (n={len(interp_df)})  |  R²={r2_k_interp:.3f}",
        os.path.join(SAVE_DIR, "parity_kappa_offgrid.png")
    )

    parity_plot(
        interp_df["mob"].values, pred_m_interp, 0, 11,
        f"M — Off-Grid (n={len(interp_df)})  |  R²={r2_m_interp:.3f}",
        os.path.join(SAVE_DIR, "parity_mob_offgrid.png")
    )

    print("[INFO] Off-grid parity plots saved.")

    # ------------------------------------------------------------------
    # 5.17  Done
    # ------------------------------------------------------------------
    elapsed = time.time() - t_start
    print(f"\n[INFO] Total runtime: {elapsed:.1f} s  ({elapsed/60:.1f} min)")
