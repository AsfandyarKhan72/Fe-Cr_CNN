def load_dataset(csv_path):
    """Load image filenames and labels (kappa, mobility) from CSV."""
    df = pd.read_csv(csv_path, header=None, names=["image_name", "kappa", "mob"])
    print(f"[INFO] Dataset loaded: {df.shape[0]} samples")
    return df


def pool_features(feats):
    """
    Apply Global Average Pooling (GAP) and Global Max Pooling (GMP) to an
    intermediate feature map and concatenate the results.

    Used identically for training and experimental images to ensure consistent
    feature representation across all inputs.

    Parameters
    ----------
    feats : np.ndarray, shape (N, H, W, C)
        Feature map output from EfficientNetB7 truncated at LAYER.

    Returns
    -------
    np.ndarray, shape (N, 2*C)
        Concatenated GAP + GMP vector. For layer 464: shape (N, 2688).
    """
    feats_tensor = tf.convert_to_tensor(feats)
    gap = GlobalAveragePooling2D()(feats_tensor)
    gmp = GlobalMaxPooling2D()(feats_tensor)
    return Concatenate()([gap, gmp]).numpy()


def extract_features(df, image_dir, pre_model, img_size):
    """
    Load and preprocess images, extract intermediate features from the
    EfficientNetB7 backbone, then apply GAP + GMP pooling.

    Parameters
    ----------
    df         : pd.DataFrame   — must contain 'image_name' column
    image_dir  : str            — directory containing image files
    pre_model  : keras.Model    — EfficientNetB7 truncated at LAYER
    img_size   : tuple          — target image size, e.g. (600, 600)

    Returns
    -------
    np.ndarray, shape (N, 2688)
    """
    images = []
    for img_name in df["image_name"]:
        path  = os.path.join(image_dir, img_name)
        image = load_img(path, target_size=img_size)
        image = img_to_array(image)
        image = np.expand_dims(image, axis=0)
        image = imagenet_utils.preprocess_input(image)
        images.append(image)

    x = np.vstack(images)
    print("[INFO] Extracting CNN features...")
    feats = pre_model.predict(x, batch_size=32)
    print(f"[INFO] Raw feature map shape : {feats.shape}")

    feats_flat = pool_features(feats)
    print(f"[INFO] After GAP + GMP       : {feats_flat.shape}")
    return feats_flat


def stratified_split(df, features, val_frac, test_frac, seed, n_bins):
    """
    Stratified three-way split (train / val / test).

    Both kappa and mobility are jointly binned into quantile intervals and the
    combined bin label is used as the stratification variable, ensuring balanced
    parameter-space coverage across all three sets.

    Parameters
    ----------
    df        : pd.DataFrame
    features  : np.ndarray
    val_frac  : float   — fraction of full dataset for validation
    test_frac : float   — fraction of full dataset for test
    seed      : int
    n_bins    : int     — number of quantile bins per parameter

    Returns
    -------
    train_df, val_df, test_df, trainX, valX, testX
    """
    kbin   = pd.qcut(df["kappa"], q=n_bins, duplicates="drop")
    mbin   = pd.qcut(df["mob"],   q=n_bins, duplicates="drop")
    strata = (kbin.astype(str) + "_" + mbin.astype(str)).astype(str)

    trainval_idx, test_idx = train_test_split(
        np.arange(len(df)),
        test_size    = test_frac,
        random_state = seed,
        stratify     = strata
    )

    adjusted_val = val_frac / (1.0 - test_frac)
    train_idx, val_idx = train_test_split(
        trainval_idx,
        test_size    = adjusted_val,
        random_state = seed,
        stratify     = strata.iloc[trainval_idx].values
    )

    print(f"[INFO] Split — train: {len(train_idx)} | "
          f"val: {len(val_idx)} | test: {len(test_idx)}")

    return (
        df.iloc[train_idx].reset_index(drop=True),
        df.iloc[val_idx].reset_index(drop=True),
        df.iloc[test_idx].reset_index(drop=True),
        features[train_idx],
        features[val_idx],
        features[test_idx]
    )


def compute_similarity_metrics(df, features, save_dir, layer):
    """
    Compute pairwise cosine similarity across the full dataset.

    Used for diagnostic purposes only — not used during training and does
    not affect model evaluation. Computes max similarity, mean similarity,
    and uniqueness (1 - max_sim) per image.

    Returns pd.DataFrame with columns: image_name, max_sim, mean_sim, uniqueness.
    """
    print("[INFO] Computing cosine similarity (full dataset)...")
    sim_matrix = cosine_similarity(features)
    np.fill_diagonal(sim_matrix, 0.0)

    sim_df = pd.DataFrame({
        "image_name" : df["image_name"].values,
        "max_sim"    : sim_matrix.max(axis=1),
        "mean_sim"   : sim_matrix.sum(axis=1) / (len(df) - 1),
        "uniqueness" : 1.0 - sim_matrix.max(axis=1)
    })

    out = os.path.join(save_dir, f"similarity_metrics_{layer}.csv")
    sim_df.to_csv(out, index=False)
    print(f"[INFO] Similarity metrics saved: {out}")
    return sim_df


def parity_plot(gt, pred, minv, maxv, title, outname):
    """Save a parity (predicted vs ground truth) scatter plot to disk."""
    fig, ax = plt.subplots(figsize=(5, 5))
    ax.scatter(gt, pred, alpha=0.7, edgecolors="k", linewidths=0.4)
    ax.plot([minv, maxv], [minv, maxv], "r--", linewidth=1.2, label="Ideal (y = x)")
    ax.set_xlim(minv, maxv)
    ax.set_ylim(minv, maxv)
    ax.set_xlabel("Ground truth", fontsize=12)
    ax.set_ylabel("Prediction",   fontsize=12)
    ax.set_title(title,           fontsize=12)
    ax.legend(fontsize=10)
    ax.grid(True, alpha=0.3)
    fig.tight_layout()
    fig.savefig(outname, dpi=300)
    plt.close(fig)


